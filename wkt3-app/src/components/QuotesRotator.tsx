  const [i, setI] = useState(0);
  useEffect(() => {
    const id = setInterval(() => setI((s) => (s + 1) % QUOTES.length), 5000);
    return () => clearInterval(id);
  }, []);
  return <div className="quotes text-sm italic">{QUOTES[i]}</div>;
